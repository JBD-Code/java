package com.access.ex01;

public class MyParent{

	private   int prv; // 같은 클래스  
			  int dft; // 같은 패키지 
	protected int prt; // 같은 패키지 + 자손 패키지 
	public 	  int pub; // 접근 제한 없음 
	
	
	public void printMembers() {
		System.out.println(prv);
		System.out.println(dft);
		System.out.println(prt);
		System.out.println(pub);
	}
}

class AccessModifierEx02 {
	public static void main(String []args){
		//access modifier 접근제어자
		//private   같은 클래스 내에서만 접근이 가능하다 
		//default   같은 패키지 내에서만 접근이 가능하다 
		//protected 같은 패키지내에서 그리고 다른 패키지의 자손 클래스에서 접근이 가능하다 
		//public 	접근 제한이 전혀 없다 
		
		MyParent p = new MyParent(); 
		//System.out.println(p.prv); Error 
		System.out.println(p.dft);
		System.out.println(p.prt);
		System.out.println(p.pub);
		
	}
}
